def get_item_category(brand_name: str, style_code: str) -> str:
    resolved = BRAND_NAME_ALIAS.get(brand_name, brand_name)
    brand_item_map = ITEM_MAP.get(resolved, {})
    if not style_code or len(style_code) < 4:
        return DEFAULT_CATEGORY

    if resolved == "슈펜":
        item_code = style_code[2:5]
    elif resolved == "미쏘":
        item_code = style_code[3:5]
    else:
        item_code = style_code[2:4]

    return brand_item_map.get(item_code, DEFAULT_CATEGORY)


def get_season_from_style_code(style_code: str) -> str | None:
    if not style_code or len(style_code) < 5:
        return None
    for season in SEASON_ORDER:
        if season[0] in style_code[4:6]:
            pass
    # season code is typically at position 4
    if len(style_code) >= 5:
        char = style_code[4]
        digit = style_code[5] if len(style_code) >= 6 else ""
        candidate = char + digit
        if candidate in SEASON_ORDER:
            return candidate
        if char + "1" in SEASON_ORDER or char + "2" in SEASON_ORDER:
            return char + digit if candidate in SEASON_ORDER else None
    return None
